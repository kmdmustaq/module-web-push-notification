declare(strict_types=1);

namespace Lof\WebPushNotification\Controller\Adminhtml\PushNotification;

class Edit extends \Lof\WebPushNotification\Controller\Adminhtml\PushNotification
{

    protected $resultPageFactory;

    /**
     * @param \Magento\Backend\App\Action\Context $context
     * @param \Magento\Framework\Registry $coreRegistry
     * @param \Magento\Framework\View\Result\PageFactory $resultPageFactory
     */
    public function __construct(
        \Magento\Backend\App\Action\Context $context,
        \Magento\Framework\Registry $coreRegistry,
        \Magento\Framework\View\Result\PageFactory $resultPageFactory
    ) {
        $this->resultPageFactory = $resultPageFactory;
        parent::__construct($context, $coreRegistry);
    }

    /**
     * Edit action
     *
     * @return \Magento\Framework\Controller\ResultInterface
     */
    public function execute()
    {
        // 1. Get ID and create model
        $id = $this->getRequest()->getParam('pushnotification_id');
        $model = $this->_objectManager->create(\Lof\WebPushNotification\Model\PushNotification::class);
        
        // 2. Initial checking
        if ($id) {
            $model->load($id);
            if (!$model->getId()) {
                $this->messageManager->addErrorMessage(__('This Pushnotification no longer exists.'));
                /** @var \Magento\Backend\Model\View\Result\Redirect $resultRedirect */
                $resultRedirect = $this->resultRedirectFactory->create();
                return $resultRedirect->setPath('*/*/');
            }
        }
        $this->_coreRegistry->register('lof_webpushnotification_pushnotification', $model);
        
        // 3. Build edit form
        /** @var \Magento\Backend\Model\View\Result\Page $resultPage */
        $resultPage = $this->resultPageFactory->create();
        $this->initPage($resultPage)->addBreadcrumb(
            $id ? __('Edit Pushnotification') : __('New Pushnotification'),
            $id ? __('Edit Pushnotification') : __('New Pushnotification')
        );
        $resultPage->getConfig()->getTitle()->prepend(__('Pushnotifications'));
        $resultPage->getConfig()->getTitle()->prepend($model->getId() ? __('Edit Pushnotification %1', $model->getId()) : __('New Pushnotification'));
        return $resultPage;
    }
}

